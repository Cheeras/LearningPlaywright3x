let name = "John Doe";// this is a String literal
//literal name refers to the value "John Doe" which is a string literal in JavaScript. 
// String literals are used to represent text and are enclosed in either single quotes (' '), 
// double quotes (" "), or backticks (` `) for template literals. 
// In this case, the variable `name` is assigned the string literal "John Doe".


let isActive = true; // this is a Boolean literal
//literal isActive refers to the value true which is a boolean literal in JavaScript. 
// Boolean literals can only have two values: true or false. 
// In this case, the variable `isActive` is assigned the boolean literal true.
// isActive is called identifier name or variable name in JavaScript.
// true is called boolean literal or variable value in JavaScript.

let age = 30; // this is a Number literal
//literal age refers to the value 30 which is a number literal in JavaScript. 
// Number literals can be integers or floating-point numbers. 
// In this case, the variable `age` is assigned the number literal 30.
// age is called identifier name or variable name in JavaScript.
// 30 is called number literal or variable value in JavaScript.

let price = 19.99; // this is a Number literal
//literal price refers to the value 19.99 which is a number literal in JavaScript. 
// Number literals can be integers or floating-point numbers. 
// In this case, the variable `price` is assigned the number literal 19.99.
// price is called identifier name or variable name in JavaScript.
// 19.99 is called number literal or variable value in JavaScript.

let nullValue = null; // this is a Null literal
//literal nullValue refers to the value null which is a null literal in JavaScript. 
// The null literal represents the intentional absence of any object value. 
// In this case, the variable `nullValue` is assigned the null literal.
// nullValue is called identifier name or variable name in JavaScript.
// null is called null literal or variable value in JavaScript.

console.log("Name:", name); // Output: Name: John Doe
console.log("Is Active:", isActive);    // Output: Is Active: true
console.log("Age:", age); // Output: Age: 30      
console.log("Price:", price); // Output: Price: 19.99
console.log("Null Value:", nullValue); // Output: Null Value: null


let undefinedValue; // this is an Undefined literal
//literal undefinedValue refers to the value undefined which is an undefined literal in JavaScript. 
// The undefined literal represents a variable that has been declared but has not yet been assigned a value. 
// In this case, the variable `undefinedValue` is declared but not assigned any value, so it holds the undefined literal.
// undefinedValue is called identifier name or variable name in JavaScript.
// undefined is called undefined literal or variable value in JavaScript.

console.log("Undefined Value:", undefinedValue); // Output: Undefined Value: undefined

//Numeric Literals
let decimalLiteral = 42; // this is a Decimal literal
//literal decimalLiteral refers to the value 42 which is a decimal literal in JavaScript.  
// Decimal literals are base-10 numbers and can be either integers or floating-point numbers.
// In this case, the variable `decimalLiteral` is assigned the decimal literal 42.
// decimalLiteral is called identifier name or variable name in JavaScript.
// 42 is called decimal literal or variable value in JavaScript.

let hexadecimalLiteral = 0x2A; // this is a Hexadecimal literal
//literal hexadecimalLiteral refers to the value 0x2A which is a hexadecimal literal in JavaScript.  
// Hexadecimal literals are base-16 numbers and are prefixed with '0x' or '0X'. 
// In this case, the variable `hexadecimalLiteral` is assigned the hexadecimal literal 0x2A, which is equivalent to 42 in decimal.
// hexadecimalLiteral is called identifier name or variable name in JavaScript.
// 0x2A is called hexadecimal literal or variable value in JavaScript.

let octalLiteral = 0o52; // this is an Octal literal
//literal octalLiteral refers to the value 0o52 which is an octal literal in JavaScript.  
// Octal literals are base-8 numbers and are prefixed with '0o' or '0O'. 
// In this case, the variable `octalLiteral` is assigned the octal literal 0o52, which is equivalent to 42 in decimal.
// octalLiteral is called identifier name or variable name in JavaScript.
// 0o52 is called octal literal or variable value in JavaScript.

let binaryLiteral = 0b101010; // this is a Binary literal
//literal binaryLiteral refers to the value 0b101010 which is a binary literal in JavaScript.  
// Binary literals are base-2 numbers and are prefixed with '0b' or '0B'. 
// In this case, the variable `binaryLiteral` is assigned the binary literal 0b101010, which is equivalent to 42 in decimal.
// binaryLiteral is called identifier name or variable name in JavaScript.
// 0b101010 is called binary literal or variable value in JavaScript.

console.log("Decimal Literal:", decimalLiteral); // Output: Decimal Literal: 42
console.log("Hexadecimal Literal:", hexadecimalLiteral);    // Output: Hexadecimal Literal: 42
console.log("Octal Literal:", octalLiteral); // Output: Octal Literal: 42
console.log("Binary Literal:", binaryLiteral); // Output: Binary Literal: 42    

//typeof operator is used to determine the datatype of a variable or literal in JavaScript

console.log("Type of name:", typeof name); // Output: Type of name: string
console.log("Type of isActive:", typeof isActive);  // Output: Type of isActive: boolean    
console.log("Type of age:", typeof age); // Output: Type of age: number
console.log("Type of price:", typeof price); // Output: Type of price: number
console.log("Type of nullValue:", typeof nullValue); // Output: Type of nullValue: object
console.log("Type of undefinedValue:", typeof undefinedValue); // Output: Type of undefinedValue: undefined    
