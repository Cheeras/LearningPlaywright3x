//Question 1 - Print numbers from 1 to 10

//for loop
//for loop syntax
//for(initialization:condition:updatation(increment/decrement))

for(let i = 1;i<=10;i++){//post increment 
    console.log(i)
}

//ICU syntax for the for loop
//I - Initilization
//C - Condition
//U - Update


for(let i = 1;i<=10;++i){//pre increment
    console.log(i)
}

//Note: in for loop updating (increment) can be pre increment or post increment 
//it does not matter becuase we are using the value in the next line
